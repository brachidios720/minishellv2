#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	i;
	int	a;
	int	b;

	i = 0;
	a = 0;
	b = 1;
	while (nptr[i] == ' ' || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			b = -1;
		i++;
	}
	while (nptr[i] <= '9' && nptr[i] >= '0')
	{
		a = a * 10 + (nptr[i] -48);
		i++;
	}
	return (a * b);
}
